# math_functions.py

def add(num_1, num_2):
    return num_1 + num_2

def subtract(num_1, num_2):
    return num_1 - num_2

def multiply(num_1, num_2):
    return num_1 * num_2

def divide(num_1, num_2):
    return num_1 / num_2

def power(num_1, num_2):
    return num_1 ** num_2

def modulus(num_1, num_2):
    return num_1 % num_2

